using System.Linq;
using TUnit.Mocks.SourceGenerator.Models;

namespace TUnit.Mocks.SourceGenerator.Builders;

internal static class MockWrapperTypeBuilder
{
    /// <summary>
    /// Whether a typed wrapper (IFoo_Mock : Mock&lt;IFoo&gt;, IFoo) can be generated for this model.
    /// False for multi-interface mocks, static-abstract types, and interfaces with indexers
    /// or ref-struct-returning properties that can't be forwarded.
    /// </summary>
    public static bool CanGenerateWrapper(MockTypeModel model)
    {
        if (model.AdditionalInterfaceNames.Length > 0 || model.HasStaticAbstractMembers)
            return false;

        if (model.Properties.Any(p => !p.IsStaticAbstract && (p.IsIndexer || p.IsRefStructReturn)))
            return false;

        return true;
    }

    public static string Build(MockTypeModel model)
    {
        if (!CanGenerateWrapper(model))
            return string.Empty;

        var writer = new CodeWriter();
        var safeName = MockImplBuilder.GetCompositeShortSafeName(model);
        var mockNamespace = MockImplBuilder.GetMockNamespace(model);
        var mockableType = MockImplBuilder.GetMockableTypeName(model);

        writer.AppendLine("// <auto-generated/>");
        writer.AppendLine("#nullable enable");
        writer.AppendLine();

        using (writer.Block($"namespace {mockNamespace}"))
        {
            var visibility = model.IsPublic ? "public" : "internal";
            using (writer.Block($"{visibility} sealed class {safeName}Mock : global::TUnit.Mocks.Mock<{mockableType}>, {model.FullyQualifiedName}"))
            {
                writer.AppendLine("[global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]");
                writer.AppendLine($"internal {safeName}Mock({mockableType} mockObject, global::TUnit.Mocks.MockEngine<{mockableType}> engine)");
                writer.AppendLine("    : base(mockObject, engine) { }");

                foreach (var method in model.Methods)
                {
                    if (method.IsStaticAbstract) continue;
                    writer.AppendLine();
                    GenerateMethodForwarding(writer, method, model);
                }

                foreach (var prop in model.Properties)
                {
                    if (prop.IsStaticAbstract) continue;
                    writer.AppendLine();
                    GeneratePropertyForwarding(writer, prop, model);
                }

                foreach (var evt in model.Events)
                {
                    if (evt.IsStaticAbstract) continue;
                    writer.AppendLine();
                    GenerateEventForwarding(writer, evt, model);
                }
            }
        }

        return writer.ToString();
    }

    private static void GenerateMethodForwarding(CodeWriter writer, MockMemberModel method, MockTypeModel model)
    {
        var interfaceName = method.ExplicitInterfaceName ?? method.DeclaringInterfaceName ?? model.FullyQualifiedName;
        var paramList = MockImplBuilder.GetParameterList(method);
        var typeParams = MockImplBuilder.GetTypeParameterList(method);
        var constraints = MockImplBuilder.GetConstraintClauses(method, forExplicitImplementation: true);
        var argPassList = MockImplBuilder.GetArgPassList(method);
        var returnType = (method.IsVoid && !method.IsAsync) ? "void" : method.ReturnType;

        if (method.IsVoid && !method.IsAsync)
        {
            using (writer.Block($"{returnType} {interfaceName}.{method.Name}{typeParams}({paramList}){constraints}"))
            {
                writer.AppendLine($"Object.{method.Name}{typeParams}({argPassList});");
            }
        }
        else
        {
            writer.AppendLine($"{returnType} {interfaceName}.{method.Name}{typeParams}({paramList}){constraints} => Object.{method.Name}{typeParams}({argPassList});");
        }
    }

    private static void GeneratePropertyForwarding(CodeWriter writer, MockMemberModel prop, MockTypeModel model)
    {
        var interfaceName = prop.ExplicitInterfaceName ?? prop.DeclaringInterfaceName ?? model.FullyQualifiedName;
        var returnType = prop.ReturnType;

        var getter = prop.HasGetter ? $"get => Object.{prop.Name}; " : "";
        var setter = prop.HasSetter ? $"set => Object.{prop.Name} = value; " : "";

        writer.AppendLine($"{returnType} {interfaceName}.{prop.Name} {{ {getter}{setter}}}");
    }

    private static void GenerateEventForwarding(CodeWriter writer, MockEventModel evt, MockTypeModel model)
    {
        var interfaceName = evt.ExplicitInterfaceName ?? evt.DeclaringInterfaceName ?? model.FullyQualifiedName;

        writer.AppendLine($"event {evt.EventHandlerTypeWithNullability} {interfaceName}.{evt.Name} {{ add => Object.{evt.Name} += value; remove => Object.{evt.Name} -= value; }}");
    }

}
