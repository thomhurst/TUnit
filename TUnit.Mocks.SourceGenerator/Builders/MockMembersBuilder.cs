            }
        }
    }

    private static string GetArgParameterList(MockMemberModel method, bool includeRefStructArgs)
    {
        var parts = new List<string>();
        foreach (var p in method.Parameters)
        {
            if (p.Direction == ParameterDirection.Out)
            {
                // Normally out params are omitted from the extension signature so callers
                // don't have to write `out _`. But when another overload of this method has
                // the same matchable-parameter signature (e.g. GenerateSasUri(perms, expires)
                // vs GenerateSasUri(perms, expires, out string)), we MUST keep the out param
                // in the signature, otherwise CS0111 fires on the generated extensions.
                if (method.KeepOutParamsInExtensionSignature)
                {
                    parts.Add($"out {p.FullyQualifiedType} {p.Name}");
                }
                continue;
            }
            if (p.IsRefStruct)
            {
                if (includeRefStructArgs)
                {
                    parts.Add($"global::TUnit.Mocks.Arguments.RefStructArg<{p.FullyQualifiedType}> {p.Name}");
                }
            }
            else
            {
                parts.Add($"global::TUnit.Mocks.Arguments.Arg<{p.FullyQualifiedType}> {p.Name}");
            }
        }
        return string.Join(", ", parts);
    }

    private static string BuildExtensionMethodParameterList(MockTypeModel model, string paramList)
    {
        var mockableType = MockImplBuilder.GetMockableTypeName(model);
        var extensionParam = $"this global::TUnit.Mocks.Mock<{mockableType}> mock";
        return string.IsNullOrEmpty(paramList) ? extensionParam : $"{extensionParam}, {paramList}";
    }

    private static void EmitReturnsAsyncOverloads(CodeWriter writer, string wrapperName, string taskType, bool isValueTask)
    {
        var taskLabel = isValueTask ? "ValueTask" : "Task";
        writer.AppendLine();
        if (isValueTask)
        {
            writer.AppendLine($"/// <summary>Return a pre-built {taskLabel} directly (e.g., from a TaskCompletionSource).</summary>");
            writer.AppendLine($"/// <remarks>The same {taskLabel} instance is returned on every call. Since {taskLabel} may only be awaited once,");
            writer.AppendLine($"/// use the factory overload if the mock will be called multiple times, or ensure the {taskLabel} is backed by a Task.</remarks>");
        }
        else
        {
            writer.AppendLine($"/// <summary>Return a pre-built {taskLabel} directly (e.g., from a TaskCompletionSource).</summary>");
        }
        writer.AppendLine($"public {wrapperName} ReturnsAsync({taskType} task) {{ EnsureSetup().ReturnsRaw(task); return this; }}");
        writer.AppendLine($"/// <summary>Return a pre-built {taskLabel} from a factory, invoked on each call.</summary>");
        writer.AppendLine($"public {wrapperName} ReturnsAsync(global::System.Func<{taskType}> taskFactory) {{ EnsureSetup().ReturnsRaw(() => (object?)taskFactory()); return this; }}");
    }

    private static void EmitEnsureSetup(CodeWriter writer, string builderType)
    {
        // CAS-based lazy init avoids the LazyInitializer Func closure and its two scratch fields.
        writer.AppendLine($"private {builderType} EnsureSetup()");
        using (writer.Block())
        {
            writer.AppendLine($"var existing = global::System.Threading.Volatile.Read(ref _builder);");
            writer.AppendLine("if (existing is not null) return existing;");
            writer.AppendLine("return EnsureSetupSlow();");
        }

        writer.AppendLine();
        // Race note: if two threads lose/win the CAS, the loser returns before the winner
        // calls AddSetup. Mock setup is sequential in normal usage (test setup phase completes
        // before invocations), so this window is not observable. If you ever need concurrent
        // setup + invocation of the same method-wrapper, reintroduce synchronization here.
        writer.AppendLine("[global::System.Runtime.CompilerServices.MethodImpl(global::System.Runtime.CompilerServices.MethodImplOptions.NoInlining)]");
        writer.AppendLine($"private {builderType} EnsureSetupSlow()");
        using (writer.Block())
        {
            writer.AppendLine("var setup = new global::TUnit.Mocks.Setup.MethodSetup(_memberId, _matchers, _memberName);");
            writer.AppendLine($"var fresh = new {builderType}(setup);");
            writer.AppendLine($"var prev = global::System.Threading.Interlocked.CompareExchange(ref _builder, fresh, null);");
            writer.AppendLine("if (prev is not null) return prev;");
            writer.AppendLine("// AddSetup runs only on the CAS winner. Setup is sequential in practice,");
            writer.AppendLine("// so a concurrent loser observing the builder before registration is benign.");
            writer.AppendLine("_engine.AddSetup(setup);");
            writer.AppendLine("return fresh;");
        }
    }

