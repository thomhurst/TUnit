using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using TUnit.Mocks.SourceGenerator.Models;

namespace TUnit.Mocks.SourceGenerator.Builders;

/// <summary>
/// Generates a single _MockMembers.g.cs file per mocked type, containing:
/// 1. Extension methods on Mock&lt;T&gt; for each method
/// 2. Extension properties on Mock&lt;T&gt; for each property (C# 14 extension blocks)
/// 3. Unified sealed classes per qualifying method (with BOTH setup and verify methods)
/// </summary>
internal static class MockMembersBuilder
{
    private const int MaxTypedParams = 8;
    private const int MaxFuncOverloadParams = 4;

    private static readonly HashSet<string> MockMemberNames = new(System.StringComparer.Ordinal)
    {
        "Object",
        "GetHashCode", "GetType", "ToString", "Equals"
    };

    public static string Build(MockTypeModel model)
    {
        var writer = new CodeWriter();
        var safeName = MockImplBuilder.GetSafeName(model.FullyQualifiedName);
        var mockableType = MockImplBuilder.GetMockableTypeName(model);
        var instanceEvents = model.Events.Where(e => !e.IsStaticAbstract).ToArray();
        var hasEvents = instanceEvents.Length > 0;

        writer.AppendLine("// <auto-generated/>");
        writer.AppendLine("#nullable enable");
        writer.AppendLine();

        using (writer.Block("namespace TUnit.Mocks.Generated"))
        {
            // Extension methods class
            using (writer.Block($"{model.Visibility} static class {safeName}_MockMemberExtensions"))
            {
                bool firstMember = true;

                // Pre-compute which methods need their `out` parameters kept in the extension
                // signature to avoid CS0111 collisions. A method needs disambiguation when
                // some other method on the model shares the same name AND the same
                // matchable-parameter signature (i.e. parameters excluding out).
                // Exclude non-static-abstract explicit interface implementations — they are
                // forwarding shims (e.g. IEnumerable.GetEnumerator) with no independent
                // setup/verify. Static abstract methods legitimately use ExplicitInterfaceName
                // for bridge interface generation and still need setup extensions.
                var instanceMethods = new EquatableArray<MockMemberModel>(
                    model.Methods.Where(m => m.ExplicitInterfaceName is null || m.IsStaticAbstract).ToImmutableArray());
                var methodsWithDisambiguation = ApplyOutDisambiguation(instanceMethods);

                // Methods
                foreach (var method in methodsWithDisambiguation)
                {
                    if (!firstMember) writer.AppendLine();
                    firstMember = false;
                    GenerateMemberMethod(writer, method, model, safeName);
                }

                // Properties -- extension properties via C# 14 extension blocks
                // (skip ref struct properties — can't use PropertyMockCall<RefStruct>)
                var memberProps = model.Properties
                    .Where(p => !p.IsIndexer && !p.IsRefStructReturn && !p.IsReturnTypeStaticAbstractInterface && (p.HasGetter || p.HasSetter) && (p.ExplicitInterfaceName is null || p.IsStaticAbstract))
                    .ToList();
                if (memberProps.Count > 0)
                {
                    if (!firstMember) writer.AppendLine();
                    firstMember = false;
                    GeneratePropertyExtensionBlock(writer, memberProps, model, safeName);
                }

                // Indexers -- expose as Item(...)/SetItem(..., value) extension methods so
                // setups and verifications can target distinct index values independently.
                // Each indexer overload (different parameter signature) gets its own pair.
                var indexers = model.Properties
                    .Where(p => p.IsIndexer && !p.IsStaticAbstract)
                    .ToList();
                foreach (var indexer in indexers)
                {
                    if (!firstMember) writer.AppendLine();
                    firstMember = false;
                    GenerateIndexerExtensionMethods(writer, indexer, model);
                }

                // Raise extension methods for events (skip static abstract)
                if (instanceEvents.Length > 0)
                {
                    if (!firstMember) writer.AppendLine();
                    firstMember = false;
                    GenerateRaiseExtensionMethods(writer, model);
                }
            }

            // Generate unified sealed classes for qualifying methods
            var instanceEventArray = new EquatableArray<MockEventModel>(
                instanceEvents.ToImmutableArray());
            foreach (var method in model.Methods)
            {
                if (method.ExplicitInterfaceName is not null && !method.IsStaticAbstract) continue;
                if (!ShouldGenerateTypedWrapper(method, hasEvents)) continue;
                writer.AppendLine();
                GenerateUnifiedSealedClass(writer, method, safeName, instanceEventArray, model.Visibility, model);
            }
        }

        return writer.ToString();
    }

    private static void EmitOutParamDefaults(CodeWriter writer, MockMemberModel method)
    {
        if (!method.KeepOutParamsInExtensionSignature) return;
        // Out params are assigned `default!` because the extension method never actually invokes
        // the mocked method — it only *configures* a setup. The out value is never observed by
        // caller code: this setup-configuration call returns a MockMethodCall, not the mocked
        // result. For reference types this suppresses the CS8625 nullable warning on an unused
        // assignment that exists solely to satisfy the `out` contract.
        foreach (var op in method.Parameters.Where(p => p.Direction == ParameterDirection.Out))
        {
            writer.AppendLine($"{op.Name} = default!;");
        }
    }

    private static bool ShouldGenerateTypedWrapper(MockMemberModel method, bool hasEvents)
    {
        if (method.IsGenericMethod) return false;

        // Async methods need a typed wrapper for the generated ReturnsAsync() method
        if (method.IsAsync) return true;

        // Exclude out params and ref struct params (can't be boxed or used as type args)
        var matchableParams = method.Parameters.Where(p => p.Direction != ParameterDirection.Out && !p.IsRefStruct).ToList();
        if (matchableParams.Count == 0)
        {
            // Include span-type ref struct out/ref params (supported via array conversion)
            var hasOutRefParams = method.Parameters.Any(p =>
                (!p.IsRefStruct || p.SpanElementType is not null) &&
                (p.Direction == ParameterDirection.Out || p.Direction == ParameterDirection.Ref));
            // Span return types need a typed wrapper for the generated Returns(SpanType) method
            var hasSpanReturn = method.SpanReturnElementType is not null;
            return hasEvents || hasOutRefParams || hasSpanReturn;
        }
        return matchableParams.Count <= MaxTypedParams;
    }

    private static string GetWrapperName(string safeName, MockMemberModel method)
        => $"{safeName}_{method.Name}_M{method.MemberId}_MockCall";

    private static string GetSafeMemberName(string name)
        => MockMemberNames.Contains(name) ? name + "_" : name;

    private static string GetCombinedTypeParameterList(MockTypeModel model, MockMemberModel method)
    {
        if (model.TypeParameters.Length == 0)
            return MockImplBuilder.GetTypeParameterList(method);

        if (method.TypeParameters.Length == 0)
            return MockImplBuilder.GetTypeParameterList(model);

        return "<" + string.Join(", ",
            method.TypeParameters.Select(tp => tp.Name)
                .Concat(model.TypeParameters.Select(tp => tp.Name))) + ">";
    }

    private static string GetCombinedConstraintClauses(MockTypeModel model, MockMemberModel method)
    {
        var modelConstraints = MockImplBuilder.GetConstraintClauses(model);
        var methodConstraints = MockImplBuilder.GetConstraintClauses(method);
        if (string.IsNullOrEmpty(modelConstraints)) return methodConstraints;
        if (string.IsNullOrEmpty(methodConstraints)) return modelConstraints;
        return methodConstraints + modelConstraints;
    }

    private static void GenerateUnifiedSealedClass(CodeWriter writer, MockMemberModel method, string safeName,
        EquatableArray<MockEventModel> events, string visibility, MockTypeModel model)
    {
        var setupReturnType = method.IsAsync && !method.IsVoid
            ? method.UnwrappedReturnType
            : method.ReturnType;

        var wrapperName = GetWrapperName(safeName, method);
        var wrapperTypeName = MockImplBuilder.GetGeneratedTypeName(wrapperName, model);
        var typeConstraints = MockImplBuilder.GetConstraintClauses(model);
        var matchableParams = method.Parameters.Where(p => p.Direction != ParameterDirection.Out && !p.IsRefStruct).ToList();
        var hasRefStructParams = method.HasRefStructParams;
        var allNonOutParams = method.Parameters.Where(p => p.Direction != ParameterDirection.Out).ToList();

        // Ref struct returns use the void wrapper (can't use ref structs as generic type args)
        if (method.IsVoid || method.IsRefStructReturn)
        {
            GenerateVoidUnifiedClass(writer, wrapperName, wrapperTypeName, typeConstraints, visibility, matchableParams, events, method.Parameters, hasRefStructParams, allNonOutParams, method.SpanReturnElementType, method.ReturnType,
                isAsync: method.IsAsync, isValueTask: method.IsValueTask);
        }
        else if (method.IsReturnTypeStaticAbstractInterface)
        {
            // Static-abstract interface returns can't be used as generic type args (CS8920)
            // Use object? to preserve .Returns() capability
            GenerateReturnUnifiedClass(writer, wrapperName, wrapperTypeName, typeConstraints, matchableParams, "object?", events, method.Parameters, hasRefStructParams, allNonOutParams, visibility);
        }
        else
        {
            GenerateReturnUnifiedClass(writer, wrapperName, wrapperTypeName, typeConstraints, matchableParams, setupReturnType, events, method.Parameters, hasRefStructParams, allNonOutParams, visibility,
                isAsync: method.IsAsync, isValueTask: method.IsValueTask, fullReturnType: method.ReturnType);
        }
    }

    private static void GenerateReturnUnifiedClass(CodeWriter writer, string wrapperCtorName, string wrapperTypeName, string typeConstraints,
        List<MockParameterModel> nonOutParams, string returnType, EquatableArray<MockEventModel> events,
        EquatableArray<MockParameterModel> allParameters, bool hasRefStructParams, List<MockParameterModel> allNonOutParams,
        string visibility,
        bool isAsync = false, bool isValueTask = false, string? fullReturnType = null)
    {
        var builderType = $"global::TUnit.Mocks.Setup.MethodSetupBuilder<{returnType}>";
        var hasOutRef = allParameters.Any(p => p.Direction == ParameterDirection.Out || p.Direction == ParameterDirection.Ref);

        writer.AppendLine("[global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]");
        using (writer.Block($"{visibility} sealed class {wrapperTypeName} : global::TUnit.Mocks.Verification.ICallVerification{typeConstraints}"))
        {
            // Fields
            writer.AppendLine("private readonly global::TUnit.Mocks.IMockEngineAccess _engine;");
            writer.AppendLine("private readonly int _memberId;");
            writer.AppendLine("private readonly string _memberName;");
            writer.AppendLine("private readonly global::TUnit.Mocks.Arguments.IArgumentMatcher[] _matchers;");
            writer.AppendLine($"private {builderType}? _builder;");
            writer.AppendLine();

            // Constructor
            writer.AppendLine($"internal {wrapperCtorName}(global::TUnit.Mocks.IMockEngineAccess engine, int memberId, string memberName, global::TUnit.Mocks.Arguments.IArgumentMatcher[] matchers)");
            using (writer.Block())
            {
                writer.AppendLine("_engine = engine;");
                writer.AppendLine("_memberId = memberId;");
                writer.AppendLine("_memberName = memberName;");
                writer.AppendLine("_matchers = matchers;");
            }

            writer.AppendLine();

            // EnsureSetup — CAS-based lazy init (see EmitEnsureSetup)
            EmitEnsureSetup(writer, builderType);

            writer.AppendLine();

            // Public self-returning setup methods
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Returns({returnType} value) {{ EnsureSetup().Returns(value); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Returns(global::System.Func<{returnType}> factory) {{ EnsureSetup().Returns(factory); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} ReturnsSequentially(params {returnType}[] values) {{ EnsureSetup().ReturnsSequentially(values); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Throws<TException>() where TException : global::System.Exception, new() {{ EnsureSetup().Throws<TException>(); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Throws(global::System.Exception exception) {{ EnsureSetup().Throws(exception); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Callback(global::System.Action callback) {{ EnsureSetup().Callback(callback); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} TransitionsTo(string stateName) {{ EnsureSetup().TransitionsTo(stateName); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Then() {{ EnsureSetup().Then(); return this; }}");

            if (isAsync && fullReturnType is not null)
            {
                EmitReturnsAsyncOverloads(writer, wrapperTypeName, fullReturnType, isValueTask);
            }

            // Typed parameter overloads (only for methods with typed params)
            if (nonOutParams.Count >= 1)
            {
                writer.AppendLine();
                if (hasRefStructParams)
                {
                    writer.AppendLine("#if NET9_0_OR_GREATER");
                    EmitTypedOverloads(writer, nonOutParams, returnType, wrapperTypeName, isAsync, fullReturnType, allNonOutParams);
                    writer.AppendLine("#else");
                    EmitTypedOverloads(writer, nonOutParams, returnType, wrapperTypeName, isAsync, fullReturnType);
                    writer.AppendLine("#endif");
                }
                else
                {
                    EmitTypedOverloads(writer, nonOutParams, returnType, wrapperTypeName, isAsync, fullReturnType);
                }
            }

            // Typed out/ref parameter setters
            if (hasOutRef)
            {
                writer.AppendLine();
                GenerateTypedOutRefMethods(writer, allParameters, wrapperTypeName);
            }

            // Typed event raises
            if (events.Length > 0)
            {
                writer.AppendLine();
                GenerateTypedEventRaises(writer, events, wrapperTypeName);
            }

            // Verify methods (ICallVerification implementation)
            writer.AppendLine();
            writer.AppendLine("// ICallVerification");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasCalled() => _engine.CreateVerification(_memberId, _memberName, _matchers).WasCalled();");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasCalled(global::TUnit.Mocks.Times times) => _engine.CreateVerification(_memberId, _memberName, _matchers).WasCalled(times);");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasCalled(global::TUnit.Mocks.Times times, string? message) => _engine.CreateVerification(_memberId, _memberName, _matchers).WasCalled(times, message);");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasCalled(string? message) => _engine.CreateVerification(_memberId, _memberName, _matchers).WasCalled(message);");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasNeverCalled() => _engine.CreateVerification(_memberId, _memberName, _matchers).WasNeverCalled();");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasNeverCalled(string? message) => _engine.CreateVerification(_memberId, _memberName, _matchers).WasNeverCalled(message);");
        }
    }

    private static void GenerateVoidUnifiedClass(CodeWriter writer, string wrapperCtorName, string wrapperTypeName, string typeConstraints, string visibility,
        List<MockParameterModel> nonOutParams, EquatableArray<MockEventModel> events,
        EquatableArray<MockParameterModel> allParameters, bool hasRefStructParams, List<MockParameterModel> allNonOutParams,
        string? spanReturnElementType = null, string? spanReturnType = null,
        bool isAsync = false, bool isValueTask = false)
    {
        var builderType = "global::TUnit.Mocks.Setup.VoidMethodSetupBuilder";
        var hasOutRef = allParameters.Any(p => p.Direction == ParameterDirection.Out || p.Direction == ParameterDirection.Ref);

        writer.AppendLine($"[global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]");
        using (writer.Block($"{visibility} sealed class {wrapperTypeName} : global::TUnit.Mocks.Verification.ICallVerification{typeConstraints}"))
        {
            // Fields
            writer.AppendLine("private readonly global::TUnit.Mocks.IMockEngineAccess _engine;");
            writer.AppendLine("private readonly int _memberId;");
            writer.AppendLine("private readonly string _memberName;");
            writer.AppendLine("private readonly global::TUnit.Mocks.Arguments.IArgumentMatcher[] _matchers;");
            writer.AppendLine($"private {builderType}? _builder;");
            writer.AppendLine();

            // Constructor — eagerly registers because void methods
            // are commonly used without chaining (e.g., mock.Log(Arg.Any<string>()) in strict mode).
            writer.AppendLine($"internal {wrapperCtorName}(global::TUnit.Mocks.IMockEngineAccess engine, int memberId, string memberName, global::TUnit.Mocks.Arguments.IArgumentMatcher[] matchers)");
            using (writer.Block())
            {
                writer.AppendLine("_engine = engine;");
                writer.AppendLine("_memberId = memberId;");
                writer.AppendLine("_memberName = memberName;");
                writer.AppendLine("_matchers = matchers;");
                writer.AppendLine("_ = EnsureSetup();");
            }

            writer.AppendLine();

            // EnsureSetup — CAS-based lazy init (see EmitEnsureSetup)
            EmitEnsureSetup(writer, builderType);

            writer.AppendLine();

            // Public self-returning setup methods
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Returns() {{ EnsureSetup().Returns(); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Throws<TException>() where TException : global::System.Exception, new() {{ EnsureSetup().Throws<TException>(); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Throws(global::System.Exception exception) {{ EnsureSetup().Throws(exception); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Callback(global::System.Action callback) {{ EnsureSetup().Callback(callback); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} TransitionsTo(string stateName) {{ EnsureSetup().TransitionsTo(stateName); return this; }}");
            writer.AppendLine($"/// <inheritdoc />");
            writer.AppendLine($"public {wrapperTypeName} Then() {{ EnsureSetup().Then(); return this; }}");

            // Span return support: generate Returns(SpanType) that stores via SetsOutParameter(-1, ...)
            if (spanReturnElementType is not null && spanReturnType is not null)
            {
                writer.AppendLine();
                writer.AppendLine($"/// <summary>Configure the return value for this span-returning method.</summary>");
                writer.AppendLine($"public {wrapperTypeName} Returns({spanReturnType} value) {{ EnsureSetup().SetsOutParameter(global::TUnit.Mocks.Setup.OutRefContext.SpanReturnValueIndex, value.ToArray()); return this; }}");
            }

            if (isAsync)
            {
                var taskType = isValueTask
                    ? "global::System.Threading.Tasks.ValueTask"
                    : "global::System.Threading.Tasks.Task";
                EmitReturnsAsyncOverloads(writer, wrapperTypeName, taskType, isValueTask);
            }

            // Typed parameter overloads (only for methods with typed params)
            if (nonOutParams.Count >= 1)
            {
                writer.AppendLine();
                if (hasRefStructParams)
                {
                    writer.AppendLine("#if NET9_0_OR_GREATER");
                    GenerateTypedCallbackOverload(writer, nonOutParams, wrapperTypeName, allNonOutParams);
                    writer.AppendLine();
                    GenerateTypedThrowsOverload(writer, nonOutParams, wrapperTypeName, allNonOutParams);
                    writer.AppendLine("#else");
                    GenerateTypedCallbackOverload(writer, nonOutParams, wrapperTypeName);
                    writer.AppendLine();
                    GenerateTypedThrowsOverload(writer, nonOutParams, wrapperTypeName);
                    writer.AppendLine("#endif");
                }
                else
                {
                    GenerateTypedCallbackOverload(writer, nonOutParams, wrapperTypeName);
                    writer.AppendLine();
                    GenerateTypedThrowsOverload(writer, nonOutParams, wrapperTypeName);
                }
            }

            // Typed out/ref parameter setters
            if (hasOutRef)
            {
                writer.AppendLine();
                GenerateTypedOutRefMethods(writer, allParameters, wrapperTypeName);
            }

            // Typed event raises
            if (events.Length > 0)
            {
                writer.AppendLine();
                GenerateTypedEventRaises(writer, events, wrapperTypeName);
            }

            // Verify methods (ICallVerification implementation)
            writer.AppendLine();
            writer.AppendLine("// ICallVerification");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasCalled() => _engine.CreateVerification(_memberId, _memberName, _matchers).WasCalled();");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasCalled(global::TUnit.Mocks.Times times) => _engine.CreateVerification(_memberId, _memberName, _matchers).WasCalled(times);");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasCalled(global::TUnit.Mocks.Times times, string? message) => _engine.CreateVerification(_memberId, _memberName, _matchers).WasCalled(times, message);");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasCalled(string? message) => _engine.CreateVerification(_memberId, _memberName, _matchers).WasCalled(message);");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasNeverCalled() => _engine.CreateVerification(_memberId, _memberName, _matchers).WasNeverCalled();");
            writer.AppendLine("/// <inheritdoc />");
            writer.AppendLine("public void WasNeverCalled(string? message) => _engine.CreateVerification(_memberId, _memberName, _matchers).WasNeverCalled(message);");
        }
    }

    private static void GenerateTypedReturnsOverload(CodeWriter writer, List<MockParameterModel> nonOutParams,
        string returnType, string wrapperName, List<MockParameterModel>? allNonOutParams = null)
    {
        var typeList = string.Join(", ", nonOutParams.Select(p => p.FullyQualifiedType));
        var funcType = $"global::System.Func<{typeList}, {returnType}>";
        var castArgs = BuildCastArgs(nonOutParams, allNonOutParams);

        writer.AppendLine("/// <summary>Configure a typed computed return value using the actual method parameters.</summary>");
        using (writer.Block($"public {wrapperName} Returns({funcType} factory)"))
        {
            writer.AppendLine($"EnsureSetup().Returns(args => factory({castArgs}));");
            writer.AppendLine("return this;");
        }
    }

    private static void EmitTypedOverloads(CodeWriter writer, List<MockParameterModel> nonOutParams,
        string returnType, string wrapperName, bool isAsync, string? fullReturnType,
        List<MockParameterModel>? allNonOutParams = null)
    {
        GenerateTypedReturnsOverload(writer, nonOutParams, returnType, wrapperName, allNonOutParams);
        if (isAsync && fullReturnType is not null)
        {
            writer.AppendLine();
            GenerateTypedReturnsAsyncOverload(writer, nonOutParams, fullReturnType, wrapperName, allNonOutParams);
        }
        writer.AppendLine();
        GenerateTypedCallbackOverload(writer, nonOutParams, wrapperName, allNonOutParams);
        writer.AppendLine();
        GenerateTypedThrowsOverload(writer, nonOutParams, wrapperName, allNonOutParams);
    }

    private static void GenerateTypedReturnsAsyncOverload(CodeWriter writer, List<MockParameterModel> nonOutParams,
        string taskType, string wrapperName, List<MockParameterModel>? allNonOutParams = null)
    {
        var typeList = string.Join(", ", nonOutParams.Select(p => p.FullyQualifiedType));
        var funcType = $"global::System.Func<{typeList}, {taskType}>";
        var castArgs = BuildCastArgs(nonOutParams, allNonOutParams);

        writer.AppendLine("/// <summary>Configure a typed computed async return value using the actual method parameters.</summary>");
        using (writer.Block($"public {wrapperName} ReturnsAsync({funcType} factory)"))
        {
            writer.AppendLine($"EnsureSetup().ReturnsRaw(args => (object?)factory({castArgs}));");
            writer.AppendLine("return this;");
        }
    }

    private static void GenerateTypedCallbackOverload(CodeWriter writer, List<MockParameterModel> nonOutParams,
        string wrapperName, List<MockParameterModel>? allNonOutParams = null)
    {
        var typeList = string.Join(", ", nonOutParams.Select(p => p.FullyQualifiedType));
        var actionType = $"global::System.Action<{typeList}>";

        writer.AppendLine("/// <summary>Execute a typed callback using the actual method parameters.</summary>");
        using (writer.Block($"public {wrapperName} Callback({actionType} callback)"))
        {
            // allNonOutParams is null when this is the primary overload (no out/ref struct subset remapping).
            // In that case the callback's parameter types match the typed Callback<T1,...> overload directly,
            // so we can register it without a wrapping closure — avoiding the object?[] allocation.
            if (allNonOutParams is null && nonOutParams.Count <= MaxTypedParams)
            {
                writer.AppendLine("EnsureSetup().Callback(callback);");
            }
            else
            {
                var castArgs = BuildCastArgs(nonOutParams, allNonOutParams);
                writer.AppendLine($"EnsureSetup().Callback(args => callback({castArgs}));");
            }
            writer.AppendLine("return this;");
        }
    }

    private static void GenerateTypedThrowsOverload(CodeWriter writer, List<MockParameterModel> nonOutParams,
        string wrapperName, List<MockParameterModel>? allNonOutParams = null)
    {
        var typeList = string.Join(", ", nonOutParams.Select(p => p.FullyQualifiedType));
        var funcType = $"global::System.Func<{typeList}, global::System.Exception>";
        var castArgs = BuildCastArgs(nonOutParams, allNonOutParams);

        writer.AppendLine("/// <summary>Configure a typed computed exception using the actual method parameters.</summary>");
        using (writer.Block($"public {wrapperName} Throws({funcType} exceptionFactory)"))
        {
            writer.AppendLine($"EnsureSetup().Throws(args => exceptionFactory({castArgs}));");
            writer.AppendLine("return this;");
        }
    }

    private static void GenerateTypedEventRaises(CodeWriter writer, EquatableArray<MockEventModel> events,
        string wrapperName)
    {
        bool first = true;
        foreach (var evt in events)
        {
            if (!first) writer.AppendLine();
            first = false;

            var paramList = evt.RaiseParameterList.Length == 0
                ? ""
                : string.Join(", ", evt.RaiseParameterList.Select(p => $"{p.FullyQualifiedType} {p.Name}"));

            string argsExpr;
            if (evt.RaiseParameterList.Length == 0)
            {
                argsExpr = "";
            }
            else if (evt.RaiseParameterList.Length == 1)
            {
                argsExpr = $", (object?){evt.RaiseParameterList[0].Name}";
            }
            else
            {
                var argNames = string.Join(", ", evt.RaiseParameterList.Select(p => p.Name));
                argsExpr = $", (object?)new object?[] {{ {argNames} }}";
            }

            writer.AppendLine($"/// <summary>Auto-raise the {evt.Name} event when this method is called.</summary>");
            writer.AppendLine($"public {wrapperName} Raises{evt.Name}({paramList}) {{ EnsureSetup().Raises(\"{evt.Name}\"{argsExpr}); return this; }}");
        }
    }

    private static void GenerateTypedOutRefMethods(CodeWriter writer, EquatableArray<MockParameterModel> allParameters, string wrapperName)
    {
        for (int i = 0; i < allParameters.Length; i++)
        {
            var param = allParameters[i];
            if (param.Direction != ParameterDirection.Out && param.Direction != ParameterDirection.Ref)
            {
                continue;
            }

            // Skip non-span ref structs (can't be boxed)
            if (param.IsRefStruct && param.SpanElementType is null)
            {
                continue;
            }

            var prefix = param.Direction == ParameterDirection.Out ? "SetsOut" : "SetsRef";
            var methodName = prefix + ToPascalCase(param.Name);
            var dirLabel = param.Direction == ParameterDirection.Out ? "out" : "ref";

            writer.AppendLine($"/// <summary>Sets the '{param.Name}' {dirLabel} parameter to the specified value when this setup matches.</summary>");
            if (param.SpanElementType is not null)
            {
                // Span types: convert to array for storage, reconstruct at invocation time
                writer.AppendLine($"public {wrapperName} {methodName}({param.FullyQualifiedType} {param.Name}) {{ EnsureSetup().SetsOutParameter({i}, {param.Name}.ToArray()); return this; }}");
            }
            else
            {
                writer.AppendLine($"public {wrapperName} {methodName}({param.FullyQualifiedType} {param.Name}) {{ EnsureSetup().SetsOutParameter({i}, {param.Name}); return this; }}");
            }
        }
    }

    private static string ToPascalCase(string name)
    {
        if (name.StartsWith("@")) name = name[1..];
        return string.IsNullOrEmpty(name) ? name : char.ToUpperInvariant(name[0]) + name[1..];
    }

    private static string BuildCastArgs(List<MockParameterModel> nonOutParams, List<MockParameterModel>? allNonOutParams = null)
    {
        if (allNonOutParams is null)
        {
            return string.Join(", ", nonOutParams.Select((p, i) => CastArg(p, i)));
        }

        var indexMap = allNonOutParams.Select((p, i) => (p, i)).ToDictionary(x => x.p, x => x.i);
        return string.Join(", ", nonOutParams.Select(p => CastArg(p, indexMap[p])));
    }

    private static string CastArg(MockParameterModel p, int index)
    {
        // For nullable types, skip the null-forgiving operator since the value can legitimately be null.
        // For non-nullable types, ! suppresses the object? -> T conversion warning.
        var bang = p.FullyQualifiedType.EndsWith("?") ? "" : "!";
        return $"({p.FullyQualifiedType})args[{index}]{bang}";
    }

