    /// <summary>
    /// Returns the input methods, with <see cref="MockMemberModel.KeepOutParamsInExtensionSignature"/>
    /// set to <c>true</c> on any method whose generated extension method would otherwise collide with
    /// another overload. Methods are grouped by (name, type-arity, matchable-parameter signature);
    /// any group with more than one entry causes its <c>out</c>-bearing members to be flagged.
    /// The matchable-parameter signature includes parameter direction (ref/in/by-value) so that
    /// overloads differing only by direction (e.g. <c>Foo(int)</c> vs <c>Foo(ref int)</c>) are not
    /// treated as collisions.
    /// </summary>
    private static IEnumerable<MockMemberModel> ApplyOutDisambiguation(EquatableArray<MockMemberModel> methods)
    {
        var flagged = new HashSet<int>();
        var byKey = new Dictionary<string, List<MockMemberModel>>(System.StringComparer.Ordinal);
        foreach (var m in methods)
        {
            var matchable = string.Join(",", m.Parameters
                .Where(p => p.Direction != ParameterDirection.Out)
                .Select(p => $"{p.Direction}:{p.FullyQualifiedType}"));
            var typeArity = m.TypeParameters.Length;
            var key = $"{m.Name}`{typeArity}({matchable})";
            if (!byKey.TryGetValue(key, out var list))
            {
                list = new List<MockMemberModel>();
                byKey[key] = list;
            }
            list.Add(m);
        }
        foreach (var group in byKey.Values)
        {
            if (group.Count < 2) continue;
            foreach (var m in group)
            {
                if (m.Parameters.Any(p => p.Direction == ParameterDirection.Out))
                {
                    flagged.Add(m.MemberId);
                }
            }
        }

        if (flagged.Count == 0)
        {
            return methods;
        }
        return methods.Select(m => flagged.Contains(m.MemberId)
            ? m with { KeepOutParamsInExtensionSignature = true }
            : m);
    }

    private static (bool UseTypedWrapper, string ReturnType, string SetupReturnType) GetReturnTypeInfo(
        MockMemberModel method, MockTypeModel model, string safeName)
    {
        var setupReturnType = method.IsAsync && !method.IsVoid
            ? method.UnwrappedReturnType
            : method.ReturnType;

        var hasEvents = model.Events.Any(e => !e.IsStaticAbstract);
        var useTypedWrapper = ShouldGenerateTypedWrapper(method, hasEvents);

        string returnType;
        if (useTypedWrapper)
        {
            returnType = MockImplBuilder.GetGeneratedTypeName(GetWrapperName(safeName, method), model);
        }
        else if (method.IsVoid || method.IsRefStructReturn)
        {
            returnType = "global::TUnit.Mocks.VoidMockMethodCall";
        }
        else if (method.IsReturnTypeStaticAbstractInterface)
        {
            returnType = "global::TUnit.Mocks.MockMethodCall<object?>";
        }
        else
        {
            returnType = $"global::TUnit.Mocks.MockMethodCall<{setupReturnType}>";
        }

        return (useTypedWrapper, returnType, setupReturnType);
    }

    private static void EmitMemberMethodBody(CodeWriter writer, MockMemberModel method, MockTypeModel model, string safeName, bool includeRefStructArgs, bool captureModelTypeParameters, bool receiverIsThis)
    {
        var (useTypedWrapper, returnType, setupReturnType) = GetReturnTypeInfo(method, model, safeName);

        var paramList = GetArgParameterList(method, includeRefStructArgs);
        var typeParams = captureModelTypeParameters
            ? MockImplBuilder.GetTypeParameterList(method)
            : GetCombinedTypeParameterList(model, method);
        var constraints = captureModelTypeParameters
            ? MockImplBuilder.GetConstraintClauses(method)
            : GetCombinedConstraintClauses(model, method);

        var safeMemberName = GetSafeMemberName(method.Name);
        var fullParamList = captureModelTypeParameters
            ? paramList
            : BuildExtensionMethodParameterList(model, paramList);

        if (method.IsReturnTypeStaticAbstractInterface)
        {
            writer.AppendLine($"/// <summary>Configure the mock setup for <c>{method.Name}</c>.</summary>");
            writer.AppendLine($"/// <returns>");
            writer.AppendLine($"/// A <see cref=\"global::TUnit.Mocks.MockMethodCall{{T}}\"/> typed as <c>object?</c> because the return type");
            writer.AppendLine($"/// contains static abstract members and cannot be used as a generic type argument (CS8920).");
            writer.AppendLine($"/// Pass a value that implements the return interface to <c>.Returns()</c> — incorrect types will cause an <see cref=\"global::System.InvalidCastException\"/> at call time.");
            writer.AppendLine($"/// </returns>");
        }

        var methodDeclarationPrefix = captureModelTypeParameters ? "public" : "public static";

        using (writer.Block($"{methodDeclarationPrefix} {returnType} {safeMemberName}{typeParams}({fullParamList}){constraints}"))
        {
            if (receiverIsThis)
            {
                writer.AppendLine("var mock = this;");
            }

            EmitOutParamDefaults(writer, method);

            // Build matchers array
            var matchableParams = includeRefStructArgs
                ? method.Parameters.Where(p => p.Direction != ParameterDirection.Out).ToList()
                : method.Parameters.Where(p => p.Direction != ParameterDirection.Out && !p.IsRefStruct).ToList();

            if (matchableParams.Count == 0)
            {
                writer.AppendLine("var matchers = global::System.Array.Empty<global::TUnit.Mocks.Arguments.IArgumentMatcher>();");
            }
            else
            {
                var matcherArgs = string.Join(", ", matchableParams.Select(p => $"{p.Name}.Matcher"));
                writer.AppendLine($"var matchers = new global::TUnit.Mocks.Arguments.IArgumentMatcher[] {{ {matcherArgs} }};");
            }

            if (useTypedWrapper)
            {
                var wrapperName = MockImplBuilder.GetGeneratedTypeName(GetWrapperName(safeName, method), model);
                writer.AppendLine($"return new {wrapperName}(global::TUnit.Mocks.MockRegistry.GetEngine(mock), {method.MemberId}, \"{method.Name}\", matchers);");
            }
            else if (method.IsVoid || method.IsRefStructReturn)
            {
                writer.AppendLine($"return new global::TUnit.Mocks.VoidMockMethodCall(global::TUnit.Mocks.MockRegistry.GetEngine(mock), {method.MemberId}, \"{method.Name}\", matchers);");
            }
            else if (method.IsReturnTypeStaticAbstractInterface)
            {
                writer.AppendLine($"return new global::TUnit.Mocks.MockMethodCall<object?>(global::TUnit.Mocks.MockRegistry.GetEngine(mock), {method.MemberId}, \"{method.Name}\", matchers);");
            }
            else
            {
                writer.AppendLine($"return new global::TUnit.Mocks.MockMethodCall<{setupReturnType}>(global::TUnit.Mocks.MockRegistry.GetEngine(mock), {method.MemberId}, \"{method.Name}\", matchers);");
            }
        }
    }

    private static List<int> GetFuncEligibleParamIndices(MockMemberModel method)
    {
        var indices = new List<int>();
        for (int i = 0; i < method.Parameters.Length; i++)
        {
            var p = method.Parameters[i];
            if ((p.Direction == ParameterDirection.In || p.Direction == ParameterDirection.In_Readonly)
                && !p.IsRefStruct)
            {
                indices.Add(i);
            }
        }
        return indices;
    }

    private static void EmitFuncOverloads(CodeWriter writer, MockMemberModel method, MockTypeModel model,
        string safeName, bool includeRefStructArgs, bool captureModelTypeParameters, bool receiverIsThis)
    {
        var eligible = GetFuncEligibleParamIndices(method);
        if (eligible.Count == 0 || eligible.Count > MaxFuncOverloadParams) return;

        int totalMasks = (1 << eligible.Count) - 1;
        for (int mask = 1; mask <= totalMasks; mask++)
        {
            writer.AppendLine();
            EmitSingleFuncOverload(writer, method, model, safeName, eligible, mask, includeRefStructArgs, captureModelTypeParameters, receiverIsThis);
        }
    }

    private static void EmitSingleFuncOverload(CodeWriter writer, MockMemberModel method, MockTypeModel model,
        string safeName, List<int> eligibleIndices, int funcMask, bool includeRefStructArgs, bool captureModelTypeParameters, bool receiverIsThis)
    {
        // Determine which parameter indices use Func<T, bool>
        var funcIndices = new HashSet<int>();
        for (int bit = 0; bit < eligibleIndices.Count; bit++)
        {
            if ((funcMask & (1 << bit)) != 0)
            {
                funcIndices.Add(eligibleIndices[bit]);
            }
        }

        var (useTypedWrapper, returnType, setupReturnType) = GetReturnTypeInfo(method, model, safeName);

        // Build mixed parameter list
        var paramParts = new List<string>();
        for (int i = 0; i < method.Parameters.Length; i++)
        {
            var p = method.Parameters[i];
            if (p.Direction == ParameterDirection.Out)
            {
                // Keep out params only when needed to disambiguate colliding overloads.
                // Callers of the disambiguated overload must write `out _` at the call site.
                if (method.KeepOutParamsInExtensionSignature)
                {
                    paramParts.Add($"out {p.FullyQualifiedType} {p.Name}");
                }
                continue;
            }

            if (funcIndices.Contains(i))
            {
                paramParts.Add($"global::System.Func<{p.FullyQualifiedType}, bool> {p.Name}");
            }
            else if (p.IsRefStruct)
            {
                if (includeRefStructArgs)
                {
                    paramParts.Add($"global::TUnit.Mocks.Arguments.RefStructArg<{p.FullyQualifiedType}> {p.Name}");
                }
            }
            else
            {
                paramParts.Add($"global::TUnit.Mocks.Arguments.Arg<{p.FullyQualifiedType}> {p.Name}");
            }
        }

        var paramList = string.Join(", ", paramParts);
        var typeParams = captureModelTypeParameters
            ? MockImplBuilder.GetTypeParameterList(method)
            : GetCombinedTypeParameterList(model, method);
        var constraints = captureModelTypeParameters
            ? MockImplBuilder.GetConstraintClauses(method)
            : GetCombinedConstraintClauses(model, method);

        var safeMemberName = GetSafeMemberName(method.Name);
        var fullParamList = captureModelTypeParameters
            ? paramList
            : BuildExtensionMethodParameterList(model, paramList);

        if (method.IsReturnTypeStaticAbstractInterface)
        {
            writer.AppendLine($"/// <summary>Configure the mock setup for <c>{method.Name}</c>.</summary>");
            writer.AppendLine($"/// <returns>");
            writer.AppendLine($"/// A <see cref=\"global::TUnit.Mocks.MockMethodCall{{T}}\"/> typed as <c>object?</c> because the return type");
            writer.AppendLine($"/// contains static abstract members and cannot be used as a generic type argument (CS8920).");
            writer.AppendLine($"/// Pass a value that implements the return interface to <c>.Returns()</c> — incorrect types will cause an <see cref=\"global::System.InvalidCastException\"/> at call time.");
            writer.AppendLine($"/// </returns>");
        }

        var methodDeclarationPrefix = captureModelTypeParameters ? "public" : "public static";

        using (writer.Block($"{methodDeclarationPrefix} {returnType} {safeMemberName}{typeParams}({fullParamList}){constraints}"))
        {
            if (receiverIsThis)
            {
                writer.AppendLine("var mock = this;");
            }

            EmitOutParamDefaults(writer, method);

            // Convert Func params to Arg<T> via implicit conversion
            foreach (var idx in funcIndices.OrderBy(i => i))
            {
                var p = method.Parameters[idx];
                var rawName = p.Name.StartsWith("@") ? p.Name[1..] : p.Name;
                writer.AppendLine($"global::TUnit.Mocks.Arguments.Arg<{p.FullyQualifiedType}> __fa_{rawName} = {p.Name};");
            }

            // Build matchers array
            var matcherExprs = new List<string>();
            for (int i = 0; i < method.Parameters.Length; i++)
            {
                var p = method.Parameters[i];
                if (p.Direction == ParameterDirection.Out) continue;
                if (!includeRefStructArgs && p.IsRefStruct) continue;

                var rawName = p.Name.StartsWith("@") ? p.Name[1..] : p.Name;
                matcherExprs.Add(funcIndices.Contains(i) ? $"__fa_{rawName}.Matcher" : $"{p.Name}.Matcher");
            }

            if (matcherExprs.Count == 0)
            {
                writer.AppendLine("var matchers = global::System.Array.Empty<global::TUnit.Mocks.Arguments.IArgumentMatcher>();");
            }
            else
            {
                writer.AppendLine($"var matchers = new global::TUnit.Mocks.Arguments.IArgumentMatcher[] {{ {string.Join(", ", matcherExprs)} }};");
            }

            // Return statement
            if (useTypedWrapper)
            {
                var wrapperName = MockImplBuilder.GetGeneratedTypeName(GetWrapperName(safeName, method), model);
                writer.AppendLine($"return new {wrapperName}(global::TUnit.Mocks.MockRegistry.GetEngine(mock), {method.MemberId}, \"{method.Name}\", matchers);");
            }
            else if (method.IsVoid || method.IsRefStructReturn)
            {
                writer.AppendLine($"return new global::TUnit.Mocks.VoidMockMethodCall(global::TUnit.Mocks.MockRegistry.GetEngine(mock), {method.MemberId}, \"{method.Name}\", matchers);");
            }
            else if (method.IsReturnTypeStaticAbstractInterface)
            {
                writer.AppendLine($"return new global::TUnit.Mocks.MockMethodCall<object?>(global::TUnit.Mocks.MockRegistry.GetEngine(mock), {method.MemberId}, \"{method.Name}\", matchers);");
            }
            else
            {
                writer.AppendLine($"return new global::TUnit.Mocks.MockMethodCall<{setupReturnType}>(global::TUnit.Mocks.MockRegistry.GetEngine(mock), {method.MemberId}, \"{method.Name}\", matchers);");
            }
        }
    }

    private static void GeneratePropertyExtensionBlock(CodeWriter writer, List<MockMemberModel> props, MockTypeModel model, string safeName)
    {
        var mockableType = MockImplBuilder.GetMockableTypeName(model);
        var typeParams = MockImplBuilder.GetTypeParameterList(model);
        var constraints = MockImplBuilder.GetConstraintClauses(model);
