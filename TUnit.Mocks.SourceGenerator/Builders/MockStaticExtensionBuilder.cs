using TUnit.Mocks.SourceGenerator.Models;

namespace TUnit.Mocks.SourceGenerator.Builders;

internal static class MockStaticExtensionBuilder
{
    public static string Build(MockTypeModel model)
    {
        if (!MockWrapperTypeBuilder.CanGenerateWrapper(model))
            return string.Empty;

        var writer = new CodeWriter();
        // Use full FQN-based safe name for the extension class (in shared TUnit.Mocks namespace, must be globally unique)
        var extensionClassName = MockImplBuilder.GetCompositeSafeName(model);
        // Use short names for the wrapper type (in namespace-specific namespace)
        var shortName = MockImplBuilder.GetCompositeShortSafeName(model);
        var mockNamespace = MockImplBuilder.GetMockNamespace(model);
        var mockableType = MockImplBuilder.GetMockableTypeName(model);

        writer.AppendLine("// <auto-generated/>");
        writer.AppendLine("#nullable enable");
        writer.AppendLine();

        using (writer.Block("namespace TUnit.Mocks"))
        {
            using (writer.Block($"public static class {extensionClassName}_MockStaticExtension"))
            {
                using (writer.Block($"extension({model.FullyQualifiedName})"))
                {
                    using (writer.Block($"public static global::{mockNamespace}.{shortName}Mock Mock(global::TUnit.Mocks.MockBehavior behavior = global::TUnit.Mocks.MockBehavior.Loose)"))
                    {
                        writer.AppendLine($"return (global::{mockNamespace}.{shortName}Mock)global::TUnit.Mocks.Mock.Of<{mockableType}>(behavior);");
                    }
                }
            }
        }

        return writer.ToString();
    }
}
