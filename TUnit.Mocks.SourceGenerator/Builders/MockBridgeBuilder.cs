using System.Linq;
using TUnit.Mocks.SourceGenerator.Models;

namespace TUnit.Mocks.SourceGenerator.Builders;

/// <summary>
/// Generates a bridge interface with Default Interface Method (DIM) implementations
/// for all static abstract members, plus a companion static engine class.
///
/// This resolves CS8920: interfaces with unresolved static abstract members cannot be
/// used as generic type arguments. The bridge interface provides the "most specific
/// implementation" via DIMs, making it usable as T in Mock&lt;T&gt; and MockEngine&lt;T&gt;.
/// </summary>
internal static class MockBridgeBuilder
{
    public static string Build(MockTypeModel model)
    {
        var writer = new CodeWriter();
        var safeName = MockImplBuilder.GetCompositeShortSafeName(model);
        var mockNamespace = MockImplBuilder.GetMockNamespace(model);

        writer.AppendLine("// <auto-generated/>");
        writer.AppendLine("#nullable enable");
        writer.AppendLine();

        using (writer.Block($"namespace {mockNamespace}"))
        {
            BuildBridgeInterface(writer, model, safeName);
            writer.AppendLine();
            BuildStaticEngineClass(writer, safeName);
        }

        return writer.ToString();
    }

    private static void BuildBridgeInterface(CodeWriter writer, MockTypeModel model, string safeName)
    {
        writer.AppendLine("[global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]");
        var visibility = model.IsPublic ? "public" : "internal";
        using (writer.Block($"{visibility} interface {safeName}Mockable : {model.FullyQualifiedName}"))
        {
            bool first = true;

            // Static abstract methods — provide DIM implementations
            foreach (var method in model.Methods)
            {
                if (!method.IsStaticAbstract) continue;
                if (!first) writer.AppendLine();
                first = false;
                GenerateStaticMethodDim(writer, method, safeName);
            }

            // Static abstract properties — provide DIM implementations
            foreach (var prop in model.Properties)
            {
                if (!prop.IsStaticAbstract) continue;
                if (prop.IsIndexer) continue;
                if (!first) writer.AppendLine();
                first = false;
                GenerateStaticPropertyDim(writer, prop, safeName);
            }

            // Static abstract events — provide no-op DIM implementations
            foreach (var evt in model.Events)
            {
                if (!evt.IsStaticAbstract) continue;
                if (!first) writer.AppendLine();
                first = false;
                GenerateStaticEventDim(writer, evt);
            }
        }
    }

    private static void BuildStaticEngineClass(CodeWriter writer, string safeName)
    {
        writer.AppendLine("[global::System.ComponentModel.EditorBrowsable(global::System.ComponentModel.EditorBrowsableState.Never)]");
        using (writer.Block($"internal static class {safeName}StaticEngine"))
        {
            writer.AppendLine("private static readonly global::System.Threading.AsyncLocal<global::TUnit.Mocks.IMockEngineAccess?> _engine = new();");
            writer.AppendLine();
            writer.AppendLine("internal static global::TUnit.Mocks.IMockEngineAccess? Engine");
            writer.OpenBrace();
            writer.AppendLine("get => _engine.Value;");
            writer.AppendLine("set => _engine.Value = value;");
            writer.CloseBrace();
        }
    }

    private static void GenerateStaticMethodDim(CodeWriter writer, MockMemberModel method, string safeName)
    {
        var signatureReturnType = (method.IsVoid && !method.IsAsync) ? "void" : method.ReturnType;
        var paramList = MockImplBuilder.GetParameterList(method);
        var typeParams = MockImplBuilder.GetTypeParameterList(method);
        var constraints = MockImplBuilder.GetConstraintClauses(method, forExplicitImplementation: true);

        using (writer.Block($"static {signatureReturnType} {method.ExplicitInterfaceName}.{method.Name}{typeParams}({paramList}){constraints}"))
        {
            GenerateStaticEngineDispatchBody(writer, method, safeName);
        }
    }

    private static void GenerateStaticPropertyDim(CodeWriter writer, MockMemberModel prop, string safeName)
    {
        writer.AppendLine($"static {prop.ReturnType} {prop.ExplicitInterfaceName}.{prop.Name}");
        writer.OpenBrace();

        if (prop.HasGetter)
        {
            writer.AppendLine("get");
            writer.OpenBrace();
            writer.AppendLine($"var __engine = {safeName}StaticEngine.Engine;");
            writer.AppendLine("if (__engine is null) return default!;");
            if (prop.IsReturnTypeStaticAbstractInterface)
            {
                writer.AppendLine($"return ({prop.ReturnType})__engine.HandleCallWithReturn<object?>({prop.MemberId}, \"get_{prop.Name}\", global::System.Array.Empty<object?>(), null)!;");
            }
            else
            {
                writer.AppendLine($"return __engine.HandleCallWithReturn<{prop.ReturnType}>({prop.MemberId}, \"get_{prop.Name}\", global::System.Array.Empty<object?>(), {prop.SmartDefault});");
            }
            writer.CloseBrace();
        }

        if (prop.HasSetter)
        {
            writer.AppendLine("set");
            writer.OpenBrace();
            writer.AppendLine($"var __engine = {safeName}StaticEngine.Engine;");
            writer.AppendLine("if (__engine is null) return;");
            writer.AppendLine($"__engine.HandleCall({prop.SetterMemberId}, \"set_{prop.Name}\", new object?[] {{ value }});");
            writer.CloseBrace();
        }

        writer.CloseBrace();
    }

    private static void GenerateStaticEventDim(CodeWriter writer, MockEventModel evt)
    {
        writer.AppendLine($"static event {evt.EventHandlerType} {evt.ExplicitInterfaceName}.{evt.Name}");
        writer.OpenBrace();
        writer.AppendLine("add { }");
        writer.AppendLine("remove { }");
        writer.CloseBrace();
    }

    private static void GenerateStaticEngineDispatchBody(CodeWriter writer, MockMemberModel method, string safeName)
    {
        writer.AppendLine($"var __engine = {safeName}StaticEngine.Engine;");

        // Initialize out parameters
        foreach (var p in method.Parameters)
        {
            if (p.Direction == ParameterDirection.Out)
            {
                writer.AppendLine($"{p.Name} = default!;");
            }
        }

        var argsArray = MockImplBuilder.EmitArgsArrayVariable(writer, method);

        if (method.IsVoid && !method.IsAsync)
        {
            writer.AppendLine("if (__engine is null) return;");
            writer.AppendLine($"__engine.HandleCall({method.MemberId}, \"{method.Name}\", {argsArray});");
            MockImplBuilder.EmitOutRefReadback(writer, method);
        }
        else if (method.IsVoid && method.IsAsync)
        {
            if (method.IsValueTask)
            {
                writer.AppendLine("if (__engine is null) return default(global::System.Threading.Tasks.ValueTask);");
            }
            else
            {
                writer.AppendLine("if (__engine is null) return global::System.Threading.Tasks.Task.CompletedTask;");
            }

            using (writer.Block("try"))
            {
                writer.AppendLine($"__engine.HandleCall({method.MemberId}, \"{method.Name}\", {argsArray});");
                MockImplBuilder.EmitOutRefReadback(writer, method);
                if (method.IsValueTask)
                {
                    writer.AppendLine("return default(global::System.Threading.Tasks.ValueTask);");
                }
                else
                {
                    writer.AppendLine("return global::System.Threading.Tasks.Task.CompletedTask;");
                }
            }
            using (writer.Block("catch (global::System.Exception __ex)"))
            {
                if (method.IsValueTask)
                {
                    writer.AppendLine("return new global::System.Threading.Tasks.ValueTask(global::System.Threading.Tasks.Task.FromException(__ex));");
                }
                else
                {
                    writer.AppendLine("return global::System.Threading.Tasks.Task.FromException(__ex);");
                }
            }
        }
        else if (method.IsAsync && method.IsReturnTypeStaticAbstractInterface)
        {
            // Async method whose unwrapped return type has static abstract members (CS8920).
            // Use object? and cast instead.
            if (method.IsValueTask)
            {
                writer.AppendLine($"if (__engine is null) return new global::System.Threading.Tasks.ValueTask<{method.UnwrappedReturnType}>(default!);");
            }
            else
            {
                writer.AppendLine($"if (__engine is null) return global::System.Threading.Tasks.Task.FromResult<{method.UnwrappedReturnType}>(default!);");
            }

            using (writer.Block("try"))
            {
                writer.AppendLine($"var __result = ({method.UnwrappedReturnType})__engine.HandleCallWithReturn<object?>({method.MemberId}, \"{method.Name}\", {argsArray}, null)!;");
                MockImplBuilder.EmitOutRefReadback(writer, method);
                if (method.IsValueTask)
                {
                    writer.AppendLine($"return new global::System.Threading.Tasks.ValueTask<{method.UnwrappedReturnType}>(__result);");
                }
                else
                {
                    writer.AppendLine($"return global::System.Threading.Tasks.Task.FromResult<{method.UnwrappedReturnType}>(__result);");
                }
            }
            using (writer.Block("catch (global::System.Exception __ex)"))
            {
                if (method.IsValueTask)
                {
                    writer.AppendLine($"return new global::System.Threading.Tasks.ValueTask<{method.UnwrappedReturnType}>(global::System.Threading.Tasks.Task.FromException<{method.UnwrappedReturnType}>(__ex));");
                }
                else
                {
                    writer.AppendLine($"return global::System.Threading.Tasks.Task.FromException<{method.UnwrappedReturnType}>(__ex);");
                }
            }
        }
        else if (method.IsAsync)
        {
            if (method.IsValueTask)
            {
                writer.AppendLine($"if (__engine is null) return new global::System.Threading.Tasks.ValueTask<{method.UnwrappedReturnType}>({method.UnwrappedSmartDefault});");
            }
            else
            {
                writer.AppendLine($"if (__engine is null) return global::System.Threading.Tasks.Task.FromResult<{method.UnwrappedReturnType}>({method.UnwrappedSmartDefault});");
            }

            using (writer.Block("try"))
            {
                writer.AppendLine($"var __result = __engine.HandleCallWithReturn<{method.UnwrappedReturnType}>({method.MemberId}, \"{method.Name}\", {argsArray}, {method.UnwrappedSmartDefault});");
                MockImplBuilder.EmitOutRefReadback(writer, method);
                if (method.IsValueTask)
                {
                    writer.AppendLine($"return new global::System.Threading.Tasks.ValueTask<{method.UnwrappedReturnType}>(__result);");
                }
                else
                {
                    writer.AppendLine($"return global::System.Threading.Tasks.Task.FromResult<{method.UnwrappedReturnType}>(__result);");
                }
            }
            using (writer.Block("catch (global::System.Exception __ex)"))
            {
                if (method.IsValueTask)
                {
                    writer.AppendLine($"return new global::System.Threading.Tasks.ValueTask<{method.UnwrappedReturnType}>(global::System.Threading.Tasks.Task.FromException<{method.UnwrappedReturnType}>(__ex));");
                }
                else
                {
                    writer.AppendLine($"return global::System.Threading.Tasks.Task.FromException<{method.UnwrappedReturnType}>(__ex);");
                }
            }
        }
        else if (method.IsReturnTypeStaticAbstractInterface)
        {
            // Return type has static abstract members — can't use as generic type argument (CS8920).
            // Use object? and cast instead.
            writer.AppendLine("if (__engine is null) return default!;");
            writer.AppendLine($"var __result = __engine.HandleCallWithReturn<object?>({method.MemberId}, \"{method.Name}\", {argsArray}, null);");
            MockImplBuilder.EmitOutRefReadback(writer, method);
            writer.AppendLine($"return ({method.ReturnType})__result!;");
        }
        else
        {
            writer.AppendLine("if (__engine is null) return default!;");
            writer.AppendLine($"var __result = __engine.HandleCallWithReturn<{method.ReturnType}>({method.MemberId}, \"{method.Name}\", {argsArray}, {method.SmartDefault});");
            MockImplBuilder.EmitOutRefReadback(writer, method);
            writer.AppendLine("return __result;");
        }
    }
}
